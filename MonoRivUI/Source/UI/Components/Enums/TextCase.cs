namespace MonoRivUI;

/// <summary>
/// An enumeration that specifies the case of text.
/// </summary>
public enum TextCase
{
    /// <summary>
    /// No case.
    /// </summary>
    None,

    /// <summary>
    /// Upper case.
    /// </summary>
    Upper,

    /// <summary>
    /// Lower case.
    /// </summary>
    Lower,

    /// <summary>
    /// Title case.
    /// </summary>
    Title,
}
